package Akkiprogarm;

public class Fibonacciseries {

	public static void main(String[] args) {
		
       int nn=5;
       
       int factorial =1;
       
       for (int i = 1; i <=nn; i++) {
    	   
    	   factorial= factorial*i;
    	   
    	  
		
	}
       System.out.println(factorial);
			
		

	}

}
